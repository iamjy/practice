 struct point makePoint(int x, int y)
 {
   struct point TMP;

   TMP.x = x;
   TMP.y = y;

   return TMP;
 }

struct point addPoint(struct point PT1, struct point PT2)
{
  PT1.x += PT2.x;
  PT1.y += PT2.y;

  return PT1;
}
