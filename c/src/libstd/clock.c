/** @brief
 *  @param
 *  @param
 *  @return
 */
 void myMemcpy(void *dst, void *src, unsigned int size)
 {
   char *s = src;
   char *d = dst;
   size_t pad = size % sizeof(int);

   for (size_t i = 0; i < size / sizeof(int); i++) {
     *(int *)d = *(int *)s;
     s += 4;
     d += 4;
   }

   for (size_t i = 0; i < pad; i++) {
     *d = *s;
     d++;
     s++;
   }
 }
