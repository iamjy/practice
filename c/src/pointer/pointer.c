#include <stdint.h>
#include <stdbool.h>
#include <stdio.h>
#include <string.h>
#include "pointer.h"

/*****************************************************************************
 * Macro definitions
 *****************************************************************************/
#define STACK_SIZE 10

/*****************************************************************************
 * Type definition
 *****************************************************************************/
typedef int stack_t;

/*****************************************************************************
 * Structures
 *****************************************************************************/
/**
 *
 */
typedef struct _struct {
  int i;

} STRUCT;

/*****************************************************************************
 * Global variables
 *****************************************************************************/

/*****************************************************************************
 * Static variables
 *****************************************************************************/
stack_t stack[STACK_SIZE];
unsigned int top;

/*****************************************************************************
 * Extern variables
 *****************************************************************************/

/*****************************************************************************
 * Function prototypes
 *****************************************************************************/

/*****************************************************************************
 * Function definitions
 *****************************************************************************/
/** @brief
 *  @param
 *  @param
 *  @return
 */
void push(int data)
{
  if (top >= STACK_SIZE)
    exit(EXIT_FAILURE);

  stack[++top] = data;
}

/** @brief
 *  @param
 *  @param
 *  @return
 */
int pop(void)
{
  if (top == 0)
    exit(EXIT_FAILURE);

  return stack[--top];
}
