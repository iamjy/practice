/*****************************************************************************
 * Static variables
 *****************************************************************************/
static pthread_mutex_t mutex = PTHREAD_MUTEX_INITIALIZER;
static int32_t i = 0;

/*****************************************************************************
 * Extern variables
 *****************************************************************************/

/*****************************************************************************
 * Function prototypes
 *****************************************************************************/

/*****************************************************************************
 * Function definitions
 *****************************************************************************/
void astall_func(void)
{
    pthread_mutex_lock(&mutex);
    i = 0;
    pthread_mutex_unlock(&mutex);
}

void *astall_thread(void *arg)
{
    while(1) {
        pthread_mutex_lock(&mutex);

        if (i > 2)
            astall_func();
        else
            i++;

        pthread_mutex_unlock(&mutex);

        printf("%d\n", i);
        sleep(1);
    }
}
