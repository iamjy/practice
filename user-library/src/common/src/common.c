void error_exit (char *message)
{
  fprintf(stderr, "%s\n", message);
  exit (-1);
}

int error_printf (char *fmt, ...)
{
  char buf[256] = {'\0',};
  va_list ap;

  strcpy(buf, "ERROR : ");
  va_start(ap, fmt);
  vsprintf(buf + strlen(buf), fmt, ap);
  va_end(ap);

  puts(buf);
}

