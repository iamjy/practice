void TxSetupSelector()
{
	static char selectorData[8]={
		0xff,0xff,0x00,0x80,
		0x0b,0xf3,0x40,0x00
	};
	if (screenSelector) return;
	screenSelector=__dpmi_allocate_ldt_descriptors(1);
	if (__dpmi_set_descriptor(screenSelector,selectorData)<0) abort();
	ScreenContext.buffer.selector=screenSelector;
}

int TxSetMode(int m)
{
	int f=m&0x80;
	m&=0x7f;
	if (!screenSelector) {
		TxSetupSelector();
	}
	if (m<0 || (m>12 && m<16) || (m>28 && m<32) || m>sizeof(modes)/sizeof(TxModeData)) return -1;
	setTextMode(modes[m].mode|f);
	if (modes[m].func) modes[m].func();
	_farsetsel(_go32_info_block.selector_for_linear_memory);
	_farnspokeb(0x487,_farnspeekb(0x487)&0x7f);
	ScreenContext.clipxmax=ScreenContext.maxx=modes[m].width-1;
	ScreenContext.clipymax=ScreenContext.maxy=modes[m].height-1;
	ScreenContext.clipxmin=ScreenContext.clipymin=0;
	ScreenContext.buffer.selector=screenSelector;
	TxSetContext(&ScreenContext);
	return 0;
}
void TxGetModeInfo(int m,int *x,int *y)
{
	if(m>sizeof(modes)/sizeof(TxModeData)) {*x=-1;return;}
	*x=modes[m].width;
	*y=modes[m].height;
}

void TxSetFont(void *font, int fntHeight, int mode)
{
	int c,l;

	AccessFont();
	_farsetsel(screenSelector);
	if (mode) {
		for (c=0; c<0x10000; c++) {
			_farnspokeb(c,((char*)font)[c]);
		}
	} else {
		for (c=0; c<256; c++) {
			for (l=0; l<fntHeight; l++) {
				_farnspokeb(c*32+l,((char*)font)[c*fntHeight+l]);
			}
		}
	}
	AccessText();
	SetFontHeight(fntHeight);
}

int TxGetFont(void *font)
{
	int c;

	AccessFont();
	_farsetsel(screenSelector);
	for (c=0; c<0x10000; c++) {
		((char*)font)[c]=_farnspeekb(c);
	}
	AccessText();
	return GetFontHeight();
}
